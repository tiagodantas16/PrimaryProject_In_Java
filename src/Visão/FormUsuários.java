    private void btn_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ExcluirActionPerformed
        int resposta =0;
        resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja Realmente Excluir?");
        if(resposta == JOptionPane.YES_OPTION){
            mod.setUsu_Id(Integer.parseInt(txt_Id.getText()));
            dao.Excluir(mod);
            
            mod.setUsu_Id(Integer.parseInt(txt_Id.getText()));
            mod.setUsu_Usuário(txt_Usuário.getText());
            mod.setUsu_Nome(txt_Nome.getText());
            mod.setUsu_Senha(txt_Nome.getText());
            mod.setUsu_Tipo((String)ComboBox_Tipo.getSelectedItem());

 
        
        txt_Usuário.setText("");
        txt_Nome.setText("");
        txt_Senha.setText("");
        txt_ConfirSenha.setText("");
        txt_Usuário.setEnabled(false);
        txt_Nome.setEnabled(false);
        txt_Senha.setEnabled(false);
        txt_ConfirSenha.setEnabled(false);
        ComboBox_Tipo.setEnabled(false);
        btn_Salvar.setEnabled(false);
        btn_Cancelar.setEnabled(false);
        btn_Novo.setEnabled(true);
        }
        
    }//GEN-LAST:event_btn_ExcluirActionPerformed

