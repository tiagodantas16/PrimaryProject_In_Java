    private void btn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SalvarActionPerformed
  
        if(txt_Usuário.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o Usuário Para Continuar!");
            txt_Usuário.requestFocus();
    }else{
            if(txt_Nome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o Usuário Para Continuar!");
            txt_Nome.requestFocus();
            }else{
            if(txt_Senha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha a Senha Para Continuar!");
            txt_Senha.requestFocus();
            }else{
            if(txt_ConfirSenha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha a Senha Para Continuar!");
            txt_ConfirSenha.requestFocus();
            
            
    }
    if (flag == 1)    {
        mod.setUsu_Usuário(txt_Usuário.getText());
        mod.setUsu_Nome(txt_Nome.getText());
        mod.setUsu_Senha(txt_Senha.getText());
        mod.setUsu_Tipo((String)ComboBox_Tipo.getSelectedItem());
        dao.Salvar(mod);
        
        txt_Usuário.setText("");
        txt_Nome.setText("");
        txt_Senha.setText("");
        txt_ConfirSenha.setText("");
        txt_Usuário.setEnabled(false);
        txt_Nome.setEnabled(false);
        txt_Senha.setEnabled(false);
        txt_ConfirSenha.setEnabled(false);
        ComboBox_Tipo.setEnabled(false);
        btn_Salvar.setEnabled(false);
        btn_Cancelar.setEnabled(false);
        btn_Novo.setEnabled(true);
        
    }else{
        
        mod.setUsu_Id(Integer.parseInt(txt_Id.getText()));
        mod.setUsu_Usuário(txt_Usuário.getText());
        mod.setUsu_Nome(txt_Nome.getText());
        mod.setUsu_Senha(txt_Senha.getText());
        mod.setUsu_Tipo((String)ComboBox_Tipo.getSelectedItem());
        dao.Editar(mod);
        
        txt_Id.setText("");
        txt_Usuário.setText("");
        txt_Nome.setText("");
        txt_Senha.setText("");
        txt_ConfirSenha.setText("");
        txt_Usuário.setEnabled(false);
        txt_Nome.setEnabled(false);
        txt_Senha.setEnabled(false);
        txt_ConfirSenha.setEnabled(false);
        ComboBox_Tipo.setEnabled(false);
        btn_Salvar.setEnabled(false);
        btn_Cancelar.setEnabled(false);
        btn_Novo.setEnabled(true);
        
    }
        
    }                
            }
        }

    }//GEN-LAST:event_btn_SalvarActionPerformed

    private void btn_PesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_PesquisarActionPerformed

        mod.setPesquisa(txt_Pesquisar.getText());
        BeansUsuários model = dao.buscaUsuario(mod);
        txt_Id.setText(String.valueOf(model.getUsu_Id()));
        txt_Usuário.setText(model.getUsu_Usuário());
        txt_Nome.setText(model.getUsu_Nome());
        txt_Senha.setText(model.getUsu_Senha());
        mod.setUsu_Tipo((String)ComboBox_Tipo.getSelectedItem());
        
        btn_Editar.setEnabled(true);
        btn_Excluir.setEnabled(true);
    }//GEN-LAST:event_btn_PesquisarActionPerformed

